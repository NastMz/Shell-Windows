    private void btnCloseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCloseMouseClicked
        this.setVisible(false);
        if (!isMainOpen) {
            openMain();
        }
    }//GEN-LAST:event_btnCloseMouseClicked

