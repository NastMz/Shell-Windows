    private void openMain() {
        if (loginFrame.getLogin().isLogin()) {
            isMainOpen = true;
            loginFrame.setVisible(false);
            MainFrame main = new MainFrame(loginFrame);
            main.setLocationRelativeTo(this);
            main.setVisible(true);
        }

    }


